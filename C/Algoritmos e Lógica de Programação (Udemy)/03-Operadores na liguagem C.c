//para adicionar quebra de linha e não sair tudo junto pode adicionar /n 

int main () {

    printf("bom dia\n");
    printf("Boa noite\n");

    return 0;
}

//desse modo ele quebra de linha com o comando /n no final 

int main () {

    int x, y;

    x = 0;
    y = 1;

    printf("%d\n", x);
    printf("%d\n", y);
    
    return 0;
}

